    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String dateString = jTextField1.getText();
        String Cno = jTextField2.getText();
        if (class_valid) {
            if (dateString.isEmpty()) {
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM");
                Date date = new Date();
                dateString = String.valueOf(dateFormat.format(date));
            }
            try {
                ResultSet resultSet = MySQL.execute("SELECT * FROM `invoice` INNER JOIN `student` ON `invoice`.`student_student_no`=`student`.`student_no` "
                        + "WHERE `datetime` LIKE '" + dateString + "%' AND `class_class_no`='" + Cno + "'");

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);

                while (resultSet.next()) {

                    Vector<String> vector = new Vector<>();
                    vector.add(resultSet.getString("student_student_no"));
                    vector.add(resultSet.getString("student.first_name") + " " + resultSet.getString("student.last_name"));
                    vector.add(resultSet.getString("value"));

                    model.addRow(vector);
                    jTable1.setModel(model);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            calculateTotal();
        } else {
            JOptionPane.showMessageDialog(this, "Invalid Class", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

