    private void calculateTotal() {
        double total = 0;
        double persentage = Integer.parseInt(jTextField3.getText());

        for (int i = 0; i < jTable1.getRowCount(); i++) {

            String price = String.valueOf(jTable1.getValueAt(i, 2));
            total = total + Integer.parseInt(price);
        }

        jLabel4.setText(String.valueOf(total));
        double instituteIncome = total * persentage / 100;
        double teacherIncome = total - instituteIncome;
        jLabel8.setText(String.valueOf(teacherIncome));
        jLabel9.setText(String.valueOf(instituteIncome));

    }

