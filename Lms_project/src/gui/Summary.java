    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        // TODO add your handling code here:
        String Csearch = jTextField2.getText();
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `class` "
                    + "INNER JOIN `subject` ON `class`.`teacher_has_subject_subject_subject_no`=`subject`.`subject_no` "
                    + "WHERE `class_no`='" + Csearch + "'");
            jLabel11.setText(" ");

            if (resultSet.next()) {
                jLabel11.setText(resultSet.getString("name"));
                jLabel11.setForeground(Color.GREEN);
                class_valid = true;
            } else {
                jLabel11.setText("No Class");
                jLabel11.setForeground(Color.RED);
                class_valid = false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField2KeyReleased

