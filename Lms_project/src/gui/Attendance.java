    private void loadAttendance() {
        try {
            String Cno = jTextField1.getText();

            ResultSet resultSet = MySQL.execute("SELECT * FROM `attendance` "
                    + "INNER JOIN `student` ON `attendance`.`student_student_no`=`student`.`student_no` "
                    + "WHERE `class_class_no`='" + Cno + "' ORDER BY `date` DESC");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("student.student_no"));
                vector.add(resultSet.getString("student.first_name") + " " + resultSet.getString("student.last_name"));
                vector.add(resultSet.getString("student.mobile"));
                vector.add(resultSet.getString("student.address"));
                vector.add(resultSet.getString("date"));

                model.addRow(vector);
                jTable1.setModel(model);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
