    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Cno = jTextField1.getText();
        String Sno = jTextField2.getText();

        if (Cno.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Class", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (Sno.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Student", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            Date date = new Date();
            String dateString = String.valueOf(dateFormat.format(date));
            
            try {

                MySQL.execute("INSERT INTO `attendance`(`date`,`attend_status_status_id`,`student_student_no`,`class_class_no`) "
                        + "VALUES('" + dateString + "','1','" + Sno + "','" + Cno + "')");

                loadAttendance();
                reset();
                jLabel4.setText(" ");

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

