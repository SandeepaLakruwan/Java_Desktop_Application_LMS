    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:

        int invoice_no = 0;
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `invoice` ORDER BY `datetime` DESC");
            if (resultSet.next()) {
                resultSet.next();
                invoice_no = Integer.parseInt(resultSet.getString("invoice_no"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        int newInvoice = invoice_no + 1;
        
        Date date = new Date();
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MMM-dd");
        String stringDate = format.format(date);

        HashMap<String, Object> parameters = new HashMap<>();
        parameters.put("Parameter1", jLabel2.getText());
        parameters.put("Parameter2", jLabel12.getText());
        parameters.put("Parameter3", jLabel13.getText());
        parameters.put("Parameter4", stringDate);
        parameters.put("Parameter5", String.valueOf(newInvoice));
        parameters.put("Parameter6", jLabel11.getText());

        try {
            JRTableModelDataSource datasource = new JRTableModelDataSource(jTable1.getModel());
            JasperPrint report = (JasperFillManager.fillReport("src/reports/Invoice.jasper", parameters, datasource));
            JasperViewer.viewReport(report, false);

        } catch (Exception e) {
            e.printStackTrace();
        }

        for (int i = 0; i < jTable1.getRowCount(); i++) {

            String month = String.valueOf(jTable1.getValueAt(i, 3));
            String Cno = String.valueOf(jTable1.getValueAt(i, 1));

            Invoice invoice = invoiceMap.get(Cno + month);
            String monthId = invoice.getMonth_id();
            String value = invoice.getValue();
            String SNo = invoice.getStdNo();
            String CNo = invoice.getClsNo();
            String datetime = invoice.getDatetime();

            try {

                MySQL.execute("INSERT INTO `invoice`(`invoice_no`,`month_id`,`value`,`student_student_no`,`class_class_no`,`datetime`) "
                        + "VALUES('" + newInvoice + "','" + monthId + "','" + value + "','" + SNo + "','" + CNo + "','" + datetime + "')");

                invoiceMap.remove(Cno + month);

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        reset();
        jTextField1.setText("");
        jLabel2.setText(" ");
        jLabel12.setText(" ");
        jLabel13.setText(" ");
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        jLabel11.setText("0.0");
    }//GEN-LAST:event_jButton6ActionPerformed

