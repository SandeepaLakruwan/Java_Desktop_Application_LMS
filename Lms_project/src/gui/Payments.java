    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Sno = jTextField1.getText();
        String month = String.valueOf(jComboBox1.getSelectedItem());
        String Cno = jTextField2.getText();
        String value = jLabel9.getText();

        if (!std_valid) {
            JOptionPane.showMessageDialog(this, "Invalid Student", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!class_valid) {
            JOptionPane.showMessageDialog(this, "Invalid Class", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (month.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Invalid Month", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            boolean found = false;

            for (int i = 0; i < jTable1.getRowCount(); i++) {

                String currentCno = String.valueOf(jTable1.getValueAt(i, 1));
                String currentMonth = String.valueOf(jTable1.getValueAt(i, 3));

                if (currentCno.equals(Cno)) {
                    if (currentMonth.equals(month)) {
                        JOptionPane.showMessageDialog(this, "Allready existed!", "Warning", JOptionPane.WARNING_MESSAGE);
                        found = true;
                    }
                }
            }

            if (!found) {
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                Date date = new Date();
                String datetime = String.valueOf(dateFormat.format(date));

                int monthId = monthMap.get(month);

                Vector<String> vector = new Vector();
                vector.add(Sno);
                vector.add(Cno);
                vector.add(jLabel3.getText());
                vector.add(month);
                vector.add(value);

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.addRow(vector);

                String stringMonthId = String.valueOf(monthId);

                Invoice invoice = new Invoice();
                invoice.setMonth_id(String.valueOf(stringMonthId));
                invoice.setValue(value);
                invoice.setStdNo(Sno);
                invoice.setClsNo(Cno);
                invoice.setDatetime(datetime);
                invoiceMap.put(Cno + month, invoice);
            }
            reset();
            calculateTotal();

        }
    }//GEN-LAST:event_jButton1ActionPerformed

