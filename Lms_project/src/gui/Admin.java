    private void loadAdmin() {
        String search = jTextField1.getText();

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `admin` INNER JOIN `gender` ON "
                    + "`admin`.`gender_id`=`gender`.`id` INNER JOIN `admin_type` ON "
                    + "`admin`.`admin_type_id`=`admin_type`.`id` WHERE `admin`.`name` LIKE '" + search + "%' ORDER BY `admin`.`name` ASC");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("admin.name"));
                vector.add(resultSet.getString("admin_type.type"));
                vector.add(resultSet.getString("address"));
                vector.add(resultSet.getString("city"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("password"));
                vector.add(resultSet.getString("gender.name"));

                model.addRow(vector);
                jTable1.setModel(model);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

