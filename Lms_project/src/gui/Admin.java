    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String name = jTextField2.getText();
        String userType = String.valueOf(jComboBox1.getSelectedItem());
        String address = jTextField4.getText();
        String city = jTextField5.getText();
        String mobile = jTextField6.getText();
        String email = jTextField7.getText();
        String password = String.valueOf(jPasswordField1.getPassword());

        String usertypeId = "";
        if (userType.equals("Super Admin")) {
            usertypeId = "1";
        } else if (userType.equals("Admin")) {
            usertypeId = "2";
        } else if (userType.equals("Cashier")) {
            usertypeId = "3";
        } else {
            usertypeId = "0";
        }

        ButtonModel genderSelection = buttonGroup1.getSelection();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (userType.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Invalid User Type", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Address", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid City", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "Invalid Mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@"
                + "[^-][A-Za-z0-9\\+-]+(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")) {
            JOptionPane.showMessageDialog(this, "Invalid Email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Password", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (genderSelection == null) {
            JOptionPane.showMessageDialog(this, "Invalid Gender", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            String genderId = genderSelection.getActionCommand();

            try {
                MySQL.execute("INSERT INTO `admin`(`name`,`email`,`password`,`admin_type_id`,`address`,`city`,`mobile`,`gender_id`) "
                        + "VALUES('" + name + "','" + email + "','" + password + "','" + usertypeId + "',"
                        + "'" + address + "','" + city + "','" + mobile + "','" + genderId + "')");

                loadAdmin();
                reset();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

