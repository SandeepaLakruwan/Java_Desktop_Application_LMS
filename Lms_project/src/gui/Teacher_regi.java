    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {

            jTable1.setEnabled(false);
            jButton2.setEnabled(false);

            int selectedRow = jTable1.getSelectedRow();

            String teacherNo = String.valueOf(jTable1.getValueAt(selectedRow, 0));
            String firstName = String.valueOf(jTable1.getValueAt(selectedRow, 1));
            jTextField2.setText(firstName);

            String lastName = String.valueOf(jTable1.getValueAt(selectedRow, 2));
            jTextField3.setText(lastName);

            String address = String.valueOf(jTable1.getValueAt(selectedRow, 3));
            jTextField4.setText(address);

            String city = String.valueOf(jTable1.getValueAt(selectedRow, 4));
            jTextField5.setText(city);

            String mobile = String.valueOf(jTable1.getValueAt(selectedRow, 5));
            jTextField6.setText(mobile);

            String email = String.valueOf(jTable1.getValueAt(selectedRow, 6));
            jTextField7.setText(email);

            String gender = String.valueOf(jTable1.getValueAt(selectedRow, 7));
            if (gender.equals("Male")) {
                jRadioButton1.setSelected(true);
            } else {
                jRadioButton2.setSelected(true);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

