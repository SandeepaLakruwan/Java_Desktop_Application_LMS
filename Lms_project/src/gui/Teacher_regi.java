    private void loadTeachers() {
        String search = jTextField1.getText();
        
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `teacher` INNER JOIN `gender` ON "
                    + "`teacher`.`gender_id`=`gender`.`id` WHERE `first_name` LIKE '"+search+"%' ORDER BY `first_name` ASC");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("teacher_no"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("address"));
                vector.add(resultSet.getString("city"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("gender.name"));

                model.addRow(vector);
                jTable1.setModel(model);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

