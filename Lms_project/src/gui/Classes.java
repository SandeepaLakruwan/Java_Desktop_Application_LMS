    private void loadClasses() {
        try {
            String search = jTextField4.getText();
            ResultSet resultSet = MySQL.execute("SELECT * FROM `class` "
                    + "INNER JOIN `teacher` ON `class`.`teacher_has_subject_teacher_teacher_no`=`teacher`.`teacher_no` "
                    + "INNER JOIN `subject` ON `class`.`teacher_has_subject_subject_subject_no`=`subject`.`subject_no` "
                    + "WHERE `name` LIKE '" + search + "%' ORDER BY `name` ASC");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("class_no"));
                vector.add(resultSet.getString("name"));
                vector.add(resultSet.getString("teacher.teacher_no"));
                vector.add(resultSet.getString("teacher.first_name") + " " + resultSet.getString("teacher.last_name"));
                vector.add(resultSet.getString("subject.subject"));
                vector.add(resultSet.getString("day"));
                vector.add(resultSet.getString("from"));
                vector.add(resultSet.getString("to"));

                model.addRow(vector);
                jTable1.setModel(model);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

