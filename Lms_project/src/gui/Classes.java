    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        String Tno = String.valueOf(jTable1.getValueAt(selectedRow, 2));
        String Cno = String.valueOf(jTable1.getValueAt(selectedRow, 0));
        String subject = String.valueOf(jComboBox1.getSelectedItem());
        String day = String.valueOf(jComboBox2.getSelectedItem());
        String from = jTextField2.getText();
        String to = jTextField3.getText();

        if (Tno.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Teacher", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (subject.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Invalid Subject", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (day.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Invalid Day", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (from.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Time From", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (to.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Time To", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                int subjectNo = subjectMap.get(subject);

                MySQL.execute("UPDATE `class` SET `name`='" + subject + "_" + Tno + "_" + day + from + to + "',"
                        + "`day`='" + day + "', `from`='" + from + "', `to`='" + to + "', "
                        + "`teacher_has_subject_teacher_teacher_no`='" + Tno + "', `teacher_has_subject_subject_subject_no`='" + subjectNo + "' "
                        + "WHERE `class_no`='" + Cno + "'");

                loadClasses();
                loadSubjectsList();
                reset();
                jTable1.setEnabled(true);
                jButton1.setEnabled(true);

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

