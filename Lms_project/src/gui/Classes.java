    private void loadStudent() {
        try {
            String search = jTextField5.getText();
            String Cno = jTextField6.getText();
            ResultSet resultSet = MySQL.execute("SELECT * FROM `student_has_class` "
                    + "INNER JOIN `student` ON `student_has_class`.`student_student_no`=`student`.`student_no` "
                    + "WHERE `class_class_no`='" + Cno + "' AND `first_name` LIKE '" + search + "%' ORDER BY `first_name` ASC");

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("student.student_no"));
                vector.add(resultSet.getString("student.first_name") + " " + resultSet.getString("student.last_name"));
                vector.add(resultSet.getString("student.mobile"));
                vector.add(resultSet.getString("student.email"));

                model.addRow(vector);
                jTable2.setModel(model);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

