    private void jTextField3KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyReleased
        // TODO add your handling code here:
        String Tsearch = jTextField3.getText();
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `teacher` WHERE `teacher_no`='" + Tsearch + "'");
            jLabel9.setText(" ");

            if (resultSet.next()) {
                jLabel9.setText(resultSet.getString("first_name") + " " + resultSet.getString("last_name"));
                jLabel9.setForeground(Color.GREEN);
                jButton5.setEnabled(true);
                jButton6.setEnabled(true);
            } else {
                jLabel9.setText("No Teacher");
                jLabel9.setForeground(Color.RED);
                jButton5.setEnabled(false);
                jButton6.setEnabled(false);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField3KeyReleased

