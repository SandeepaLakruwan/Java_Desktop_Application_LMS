    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String subject = jTextField1.getText();
        String description = jTextArea1.getText();
        String price = jTextField2.getText();

        if (subject.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Subject", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (description.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Description", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Price", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                MySQL.execute("INSERT INTO `subject`(`subject`,`description`,`price`) VALUES('" + subject + "',"
                        + "'" + description + "','" + price + "')");

                loadSubjects();
                loadSubjectsList();
                reset();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        String subjectNo = String.valueOf(jTable1.getValueAt(selectedRow, 0));
        String subject = jTextField1.getText();
        String description = jTextArea1.getText();
        String price = jTextField2.getText();

        if (subject.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Subject", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (description.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Description", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Price", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                MySQL.execute("UPDATE `subject` SET `subject`='" + subject + "',"
                        + "`description`='" + description + "',"
                        + "`price`='" + price + "' WHERE `subject_no`='" + subjectNo + "'");

                loadSubjects();
                loadSubjectsList();
                reset();

                jTable1.setEnabled(true);
                jButton1.setEnabled(true);

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

