    private void loadTeachers() {

        String sid = String.valueOf(jComboBox1.getSelectedItem());
        int subjectNo = subjectMap.get(sid);

        try {
            String search = jTextField4.getText();

            ResultSet resultSet = MySQL.execute("SELECT * FROM `teacher` "
                    + "INNER JOIN `teacher_has_subject` ON `teacher`.`teacher_no`=`teacher_has_subject`.`teacher_teacher_no` "
                    + "WHERE `subject_subject_no`='" + subjectNo + "' AND `first_name` LIKE '" + search + "%' ORDER BY `first_name` ASC");

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("teacher_no"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("email"));

                model.addRow(vector);
                jTable2.setModel(model);
                jTextField3.setEditable(true);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

