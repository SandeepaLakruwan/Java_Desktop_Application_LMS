    private void loadSubjects() {
        try {
            String search = jTextField5.getText();

            ResultSet resultSet = MySQL.execute("SELECT * FROM `subject` WHERE `subject` LIKE '" + search + "%' ORDER BY `subject` ASC");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("subject_no"));
                vector.add(resultSet.getString("subject"));
                vector.add(resultSet.getString("description"));
                vector.add(resultSet.getString("price"));

                model.addRow(vector);
                jTable1.setModel(model);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

